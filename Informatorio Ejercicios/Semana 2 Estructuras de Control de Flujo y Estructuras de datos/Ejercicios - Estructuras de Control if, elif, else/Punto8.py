#Escribir un programa que pida al usuario una cadena de caracteres y muestre
#por pantalla si contiene la letra "a".

cadena_caracteres = input("Escriba una cadena de carácteres: ")

if ("a" in cadena_caracteres):
    print("La cadena de carácteres si contiene la letra a.")
else:
    print("La cadena de carácteres no contiene la letra a.")
