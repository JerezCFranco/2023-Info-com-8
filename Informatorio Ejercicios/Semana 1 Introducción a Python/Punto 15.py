#Celsius y luego imprima la temperatura equivalente en grados Fahrenheit.
#La fÃ³rmula para convertir de Celsius a Fahrenheit es: F = (C * 1.8) + 32.

temperatura_c = float(input("Ingrese una temperatura en grados Celsius: "));
celsius_a_Fahrenheit = (temperatura_c*1.8)+32;
print(F"La temperatura de {temperatura_c} grados Celsius, es equivalente a {celsius_a_Fahrenheit} grados Fahrenheit.");